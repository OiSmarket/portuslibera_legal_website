let currentLang = 'ua'; // Default language

function setLang(lang) {
  currentLang = lang;
  const t = translations[lang];

  document.body.style.transition = 'opacity 0.25s ease';
  document.body.style.opacity = '50%';

  setTimeout(() => {

  // Update all data-i18n elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (t[key] !== undefined) el.innerHTML = t[key];
    });

  // Update placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      if (t[key] !== undefined) el.placeholder = t[key];
    });

  // Update lang buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
      btn.classList.remove('active');
      if (btn.textContent.trim().toLowerCase() === lang) btn.classList.add('active');
    });

  // Update html lang
  document.documentElement.lang = lang;

  // Update submit button if success state
  const btn = document.getElementById('submitBtn');
    if (!btn.classList.contains('success')) btn.textContent = t['form_submit'];
    document.body.style.opacity = '1';
  }, 250);
}

function toggleMenu() {
  const menu = document.getElementById('mobileMenu');
  const burger = document.querySelector('.burger');
  menu.classList.toggle('open');
  burger.classList.toggle('open');
}

function showTab(id) {
  document.querySelectorAll('.tab-content').forEach(t => t.classList.remove('active'));
  document.querySelectorAll('.tabs-scroll .tab-btn').forEach(b => b.classList.remove('active'));
  document.getElementById('tab-' + id).classList.add('active');
  event.target.classList.add('active');
}

function toggleOtherBasis(select) {
  const group = document.getElementById('other-basis-group');
  const textarea = document.getElementById('other-basis-text');
  if (select.value === 'other') {
    group.style.display = 'block';
    textarea.required = true;
  } else {
    group.style.display = 'none';
    textarea.required = false;
    textarea.value = '';
  }
}

function submitForm(e) {
  e.preventDefault();
  const btn = document.getElementById('submitBtn');
  btn.textContent = translations[currentLang]['form_success'];
  btn.classList.add('success');
  setTimeout(() => {
    btn.textContent = translations[currentLang]['form_submit'];
    btn.classList.remove('success');
  }, 3000);
}

document.addEventListener('click', function(e) {
  const menu = document.getElementById('mobileMenu');
  const burger = document.querySelector('.burger');
  if (!menu.contains(e.target) && !burger.contains(e.target)) {
    menu.classList.remove('open');
  }
});

const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            entry.target.classList.add('visible');
            observer.unobserve(entry.target);
        }
    });
}, { threshold: 0.15 });

document.querySelectorAll('.animate-on-scroll').forEach(el => observer.observe(el));
